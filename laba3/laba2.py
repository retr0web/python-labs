"""
2. Напишіть програму для отримання рядка Фібоначчі від 0 до n, де n -
ціле число. Послідовність Фібоначчі - це серія чисел 0, 1, 1, 2, 3, 5, 8,
13, 21, .... Кожне наступне число знайдено шляхом додавання двох
чисел перед ним.

Автор: Мовчан Микола
"""

fibonacci_list = []
a, b = 0, 1

n = int(input("Введіть границю для послідовності Фібоначчі: "))

while a <= n:
    fibonacci_list.append(a)
    a, b = b, a + b

print(f"Послідовність з границею {n}: {fibonacci_list}")